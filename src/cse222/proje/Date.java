package cse222.proje;

public class Date {
	/**
	 * Holds the day
	 */
	private int day;
	/**
	 * Holds the month
	 */
	private int month;
	/**
	 * Holds the year
	 */
	private int year;
	/**
	 * Holds time
	 */
	private double time;
	
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public double getTime() {
		return time;
	}
	public void setTime(double time) {
		this.time = time;
	}
	
	
}
